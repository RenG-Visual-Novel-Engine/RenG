var stack = 0

func trace(function string) string {
	stack += 1
	fmt.Println("BEGIN " + function)
	for i := 0; i < stack; i++ {
		fmt.Print("    ")
	}
	return function
}

func untrace(function string) {
	stack -= 1
	fmt.Println("\b\b\b\b" + "END " + function)
	for i := 0; i < stack; i++ {
		fmt.Print("    ")
	}
}
